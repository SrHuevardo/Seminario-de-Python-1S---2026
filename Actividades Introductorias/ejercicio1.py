"""Escribe un programa que solicite al usuario su año de nacimiento y muestre en qué año cumplirá 18, 21 y 100 años.

"""
edad = int(input("Ingrese su año de nacimiento: "))

print("Usted cumplira 18 años en el anio: ", edad + 18)
print("Usted cumplira 21 años en el anio: ", edad + 21)
print("Usted cumplira 100 años en el anio: ", edad + 100)

